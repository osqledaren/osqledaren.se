$('.pod_wrap').click(function(){
	$('.episodes').fadeIn(100);
	$('#main').css({
		'filter':'blur(10px)',
		'-webkit-filter':'blur(10px)',
		'-moz-filter':'blur(10px)',
		'-o-filter':'blur(10px)',
		'-ms-filter':'blur(10px)'
	});
	/*$('.pods_wrapper').css('filter','url(#blur)');
	$('.pods_wrapper').css('filter','progid:DXImageTransform.Microsoft.Blur(PixelRadius="10")');*/
});
$('.episodes').click(function(){
	$('.episodes').fadeOut(100);
	$('#main').css({
		'filter':'none',
		'-webkit-filter':'blur(0)',
		'-moz-filter':'blur(0)',
		'-o-filter':'blur(0)',
		'-ms-filter':'blur(0)'
	});
});